const generateRepaymentSchedule = (
  totalEmis: number,
  emiAmount: number,
  principal: number,
  rate: number,
  paidCount: number
): RepaymentEntry[] => {
  const monthlyRate = rate / 12 / 100;
  let balance = principal;
  const schedule: RepaymentEntry[] = [];

  for (let i = 1; i <= totalEmis; i++) {
    const interest = Math.round(balance * monthlyRate);
    const principalPart = Math.round(emiAmount - interest);
    balance = Math.max(0, balance - principalPart);

    const month = ((2 + i) % 12) + 1;
    const year = 2025 + Math.floor((2 + i) / 12);
    const dueDate = `${year}-${String(month).padStart(2, '0')}-05`;

    let status: RepaymentEntry['status'] = 'upcoming';
    if (i <= paidCount) status = 'paid';
    else if (i === paidCount + 1) status = 'current';

    schedule.push({
      month: i,
      dueDate,
      amount: emiAmount,
      principal: principalPart,
      interest,
      balance,
      status,
    });
  }
  return schedule;
};

